public class Vaitro {
    private int id;
    private String vaitro;

    public Vaitro() {
    }

    public Vaitro(int id, String vaitro) {
        this.id = id;
        this.vaitro = vaitro;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getVaitro() {
        return vaitro;
    }

    public void setVaitro(String vaitro) {
        this.vaitro = vaitro;
    }
    
    
    
}
